package operator_in_java;

public class Assignment_operator {

	public static void main(String[] args) {
		
		//1
		int x = 5;
		
		//2
		x += 3;
		System.out.println(x);
		
		//3
		x &= 3;
	    System.out.println(x);
	    
	    //4
	    x >>= 3;
	    System.out.println(x);
	}

}
