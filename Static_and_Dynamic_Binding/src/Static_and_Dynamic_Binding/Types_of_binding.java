package Static_and_Dynamic_Binding;
public class Types_of_binding {

	public static void main(String[] args) {
		System.out.println("There are two types of binding");
		System.out.println("1.Static Binding (also known as Early Binding)");
		System.out.println("2.Dynamic Binding (also known as Late Binding)");
		
		System.out.println("when type of object is determine at compiled time then it is known as static binding");
		System.out.println("when type of object is determine at run time then it is known as dynamic binding");
		
	}

}
