package operator_in_java;

public class comparison_or_relational {

	public static void main(String[] args) {
		int x = 5;
	    int y = 3;
	    System.out.println(x == y);
	    System.out.println(x != y);
	    System.out.println(x > y); 
	    System.out.println(x < y); 
	    System.out.println(x >= y);
	    System.out.println(x <= y);

	}

}
