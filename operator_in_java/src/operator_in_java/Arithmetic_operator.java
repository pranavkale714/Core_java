package operator_in_java;

public class Arithmetic_operator {

	public static void main(String[] args) {
		  int x = 5;
		  int y = 3;
		  
		  //addition
		  System.out.println(x + y);
		  
		  //division
		  System.out.println(x-y);
		  
		  //multiplication
		  System.out.println(x*y);
		  
		  //division
		  System.out.println("Divides one value by another");
		  System.out.println(x/y);
		  
		  //modulus
		  System.out.println("Returns the division remainder");
		  System.out.println(x%3);
		  
		  //increment
		  System.out.println("Increases the value of a variable by 1");
		  System.out.println(--x);
		  System.out.println(x--);
		  System.out.println(x);
		  
		  //decrement
		  System.out.println("Increases the value of a variable by 1");
		  System.out.println(++x);
		  System.out.println(x++);
		  System.out.println(x);
		  
		  
	     
	     
	        
	      
	      
		  
		  
		  

	}

}
