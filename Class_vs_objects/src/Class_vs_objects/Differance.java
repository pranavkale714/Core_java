package Class_vs_objects;

public class Differance {
	    public static void main(String[] args) {
	        System.out.println("1. Class is used as a template for declaring and creating the objects.");
	        System.out.println("1. Object is an instance of a class.");

	        System.out.println("2. When a class is created, no memory is allocated.");
	        System.out.println("2. Objects are allocated memory space whenever they are created.");

	        System.out.println("3. The class has to be declared first and only once.");
	        System.out.println("3. An object is created many times as per requirement.");

	        System.out.println("4. A class cannot be manipulated as it is not available in memory.");
	        System.out.println("4. Objects can be manipulated.");

	        System.out.println("5. A class is a logical entity.");
	        System.out.println("5. An object is a physical entity.");

	        System.out.println("6. A class is declared with the 'class' keyword.");
	        System.out.println("6. An object is created with 'new' keyword in Java.");

	        System.out.println("7. Class does not contain any values associated with the fields.");
	        System.out.println("7. Each object has its own values, which are associated with it.");

	        System.out.println("8. A class binds data and methods together as a single unit.");
	        System.out.println("8. Objects are like variables of the class.");

	        System.out.println("9. Example: Class - Bike");
	        System.out.println("9. Example: Object - Ducati, Suzuki, Kawasaki");
	    }
	}
